// more that takes 1 argument and add 1 to it
const more = (num) => num + 1;

// less function that takes 1 argument and substract 1 to it
const less = (num) => num - 1;

// add function that takes 2 arguments and add them
const add = (num1, num2) => num1 + num2;
// sub function that takes 2 arguments and substract them
const sub = (num1, num2) => num1 - num2;
